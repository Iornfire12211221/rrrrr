// Функция для получения ориентира на основе адреса
export const getLandmarkForAddress = (address: string): string => {
  const lowerAddress = address.toLowerCase();
  
  // Ищем совпадения в карте адресов
  for (const [pattern, landmarks] of Object.entries(ADDRESS_TO_LANDMARK_MAP)) {
    const regex = new RegExp(pattern, 'i');
    if (regex.test(lowerAddress)) {
      return landmarks[Math.floor(Math.random() * landmarks.length)];
    }
  }
  
  // Дополнительная логика для конкретных номеров домов и улиц
  if (lowerAddress.includes('карла маркса')) {
    // Для проспекта Карла Маркса выбираем ориентиры в центре
    const centralLandmarks = [
      'около супермаркета "Пятёрочка" на Карла Маркса',
      'около центра города',
      'около администрации Кингисеппского района',
      'около центральной библиотеки им. А.С. Пушкина'
    ];
    return centralLandmarks[Math.floor(Math.random() * centralLandmarks.length)];
  }
  
  if (lowerAddress.includes('воровского')) {
    const vorovskogoLandmarks = [
      'около супермаркета "Пятёрочка" на Воровского',
      'около отделения Сбербанка на Воровского',
      'около новостроек на Воровского'
    ];
    return vorovskogoLandmarks[Math.floor(Math.random() * vorovskogoLandmarks.length)];
  }
  
  // Проверяем ключевые слова в адресе для общих категорий
  if (lowerAddress.includes('школ') || lowerAddress.includes('гимназ')) {
    const schoolLandmarks = KINGISEPP_LANDMARKS.filter(l => l.includes('школы') || l.includes('гимназии'));
    return schoolLandmarks[Math.floor(Math.random() * schoolLandmarks.length)];
  }
  
  if (lowerAddress.includes('больниц') || lowerAddress.includes('поликлиник')) {
    const medicalLandmarks = KINGISEPP_LANDMARKS.filter(l => l.includes('ЦРБ') || l.includes('поликлиники'));
    return medicalLandmarks[Math.floor(Math.random() * medicalLandmarks.length)];
  }
  
  if (lowerAddress.includes('магазин') || lowerAddress.includes('торгов')) {
    const shopLandmarks = KINGISEPP_LANDMARKS.filter(l => l.includes('ТЦ') || l.includes('Пятерочки') || l.includes('Магнита'));
    return shopLandmarks[Math.floor(Math.random() * shopLandmarks.length)];
  }
  
  if (lowerAddress.includes('вокзал') || lowerAddress.includes('станц')) {
    const transportLandmarks = KINGISEPP_LANDMARKS.filter(l => l.includes('вокзала') || l.includes('автостанции'));
    return transportLandmarks[Math.floor(Math.random() * transportLandmarks.length)];
  }
  
  if (lowerAddress.includes('церков') || lowerAddress.includes('собор')) {
    const religiousLandmarks = KINGISEPP_LANDMARKS.filter(l => l.includes('собора') || l.includes('церкви'));
    return religiousLandmarks[Math.floor(Math.random() * religiousLandmarks.length)];
  }
  
  if (lowerAddress.includes('парк') || lowerAddress.includes('сквер')) {
    const parkLandmarks = KINGISEPP_LANDMARKS.filter(l => l.includes('парка') || l.includes('сквера'));
    return parkLandmarks[Math.floor(Math.random() * parkLandmarks.length)];
  }
  
  // Если не найдено совпадений, возвращаем случайный ориентир
  return getRandomLandmark();
};